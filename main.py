mood = input("Enter a state of mind: ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Let's go out for beers!")
elif mood == "excited":
    print("Oh my God! Me too!")
elif mood == "relaxed":
    print("You need to recharge your batteries.")
else:
    print("I don't recognize this mood.")